      <img src="img/icon-info.png"/>
      <p class="title">O que é democracia direta?</p>
      <p class="text">Somos uma <b>Organização Não Governamental</b> que quer o melhor para o Brasil e sua população como um todo. Temos como meta ouvir o maior número de brasileiros e fazer com que eles possam ajudar a decidir como serão feitas as leis que regem o país, as melhorias no transporte público, na saúde, em sua vida como um todo. Para ter, realmente, seu dinheiro sendo investido em serviços públicos de qualidade.<br>
        Desenvolvemos, disponibilizamos e manteremos na rede um software para que se discuta como viabilizar essas melhorias e quais seriam. Todos que tenham acesso à internet poderão fazer uso do software.
        Nosso servidor sempre estará disponível, com livre acesso para todos. Assim conseguiremos evitar possíveis fraudes, manipulação em textos e votações.<br>
        Esse meio de discussão política foi criado para que não aconteçam influências externas. Como por exemplo, de grandes empresas interessadas na aprovação de alguma medida que visa o povo.<br>
        Disso o software está livre, se o tema for de interesse, a maioria prevalecerá para que ele chegue aos políticos. Somente a parte interessada, o povo, será beneficiada.</p>
      </div>
      <div class="col-md-4">
        <img src="img/icon-how.png"/>
        <p class="title">Como vai funcionar</p>
        <p class="text">Cada cidadão poderá criar seu cadastro disponibilizando seus dados pessoais, como R.G., C.P.F., título de eleitor, etc. No caso de possuir títulos, poderá informar seu número de registro profissional e será identificado em suas discussões.
          Feito o cadastro você pode participar de posts já criados dando sua opinião, mostrando o porquê acredita que isso seja o melhor para a nação. Como também pode criar um assunto novo e iniciar outra discussão. Com base no que irá defender e fazendo o máximo para convencer outros participantes.
          Quando se cria um novo post, começa o debate entre os usuários e o texto irá para votação, como funciona o Senado e a Câmara dos Deputados. Sendo aprovado e chegando em um consenso, a ONG se responsabiliza em pressionar nossos políticos para que eles analisem, discutam e coloquem em pauta esse texto.
          Todo e qualquer debate será livre, sem mediadores. Isso promove a livre expressão e discussão que é um dos motivos da criação desse software.
          Será totalmente apartidário. O software foi desenvolvido para que todos, independente de sua posição política, possam se expressar. Os partidos e seus afiliados também terão acesso para que possam acompanhar o que está sendo pautado e discutido.</p>
        </div>
        <div class="col-md-4">
          <img src="img/icon-why.png"/>
          <p class="title">Para que essa ferramenta?</p>
          <p class="text">Para que se crie a discussão entre o povo e consiga levar aos que comandam a nação a nossa real vontade.<br><br>
            Somente assim conseguiremos fazer com que nossos Deputados e Senadores ouçam a opinião de quem os elegeu. Caso contrário eles teriam que sair às ruas todos os dias, perguntar para cada um o que ele quer que melhore e como seja feito.<br><br>
            Com esse software isso será muito facilitado, iremos nos aproximar ainda mais da Câmara e do Senado.</p>
          </div>
